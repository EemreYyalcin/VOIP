package emreylc.sipmessage.sdp;

public class SDPAttribute {

    private String key;
    private String value;

    private SDPAttribute sdpAttribute;

    
    
    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    public SDPAttribute getSdpAttribute() {
        return sdpAttribute;
    }

    public void setSdpAttribute(SDPAttribute sdpAttribute) {
        this.sdpAttribute = sdpAttribute;
    }

    
    
}
